R&eacute;alis&eacute; en 1996, ce premier long m&eacute;trage de
<?php Lien("Gens/GansChristophe/", "Christophe Gans"); ?> est adapt&eacute; d'un manga
homonyme tr&egrave;s c&eacute;l&egrave;bre, cr&eacute;&eacute; par Kazuo Koike pour l'histoire et Ry&ocirc;ichi Ikegami
pour le graphisme.
<P>
Christophe Gans a adapt&eacute; lui-m&ecirc;me le manga avec l'aide de Thierry Cazals.
<P>
D&egrave;s l'annonce de son projet d'adapter ce sujet, Christophe Gans a re&ccedil;u le
soutien passionn&eacute; de ses producteurs Samuel Hadida, producteur notamment de
<?php Lien("Films/TrueRomance/", "True romance"); ?>,
et <?php Lien("Films/KillingZoe/", "Killing Zo&eacute;"); ?> et distributeur en
France de <?php Lien("Films/Seven/", "Seven"); ?>, et Brian Yuzna.
<P>
<IMG SRC="Films/CryingFreeman/italien.gif" ALIGN=RIGHT>
Tr&egrave;s inspir&eacute; dans sa mani&egrave;re de filmer par les grands r&eacute;alisateurs hong-kongais,
dont <?php Lien("Gens/HarkTsui/", "Tsui Hark"); ?> et
<?php Lien("Gens/WooJohn/", "John Woo"); ?>, Christophe Gans s'est assur&eacute;
la colaboration du monteur f&eacute;tiche de ce dernier, David Wu. On retrouve ainsi
des encha&icirc;nements typique des films de John Woo tout au long de <I>Crying
Freeman</I>, dont les successions de plans au ralenti et un nombre
impressionant de points de vue diff&eacute;rents pour une m&ecirc;me sc&egrave;ne.
<P>
<IMG SRC="Films/CryingFreeman/yo.gif" ALIGN=LEFT>
Ayant la volont&eacute; de rester tr&egrave;s fid&egrave;le &agrave; son mod&egrave;le, Christophe Gans a choisi
Mark Dacascos pour incarner Yo Hinomura, c'est &agrave; dire le Freeman. Apr&egrave;s
avoir &eacute;t&eacute; champion d'Italie et d'Europe de Kung-fu de 1980 &agrave; 1982, puis h&eacute;ros
de s&eacute;ries t&eacute;l&eacute;vis&eacute;es, Mark Dacascos d&eacute;bute au cin&eacute;ma dans <I>Dim Sum</I> de
Wayne Wang en 1985, puis devient acteur muscl&eacute; dans <I>American Samoura&iuml;</I>
de Sam Firstenberg en 1993, <I>Double Dragon</I> de James Yukich
en 1994 et <I>Only the strong</I> de Sheldon Lettich en 1995.
<P>
Pour le personnage de Yo, Christophe Gans a en t&ecirc;te un m&eacute;lange entre Bruce Lee
et Alain Delon, la puissance f&eacute;line du premier alli&eacute;e &agrave; la beaut&eacute; t&eacute;n&eacute;breuse
du second. Il pense aussi &agrave; Tyrone Power, inoubliable <I>Zorro</I>. Il demande
donc &agrave; Mark Dacascos de perdre dix kilos, afin de gagner l'apparence fi&eacute;vreuse
et asc&eacute;tique qui sied au personnage. L'autre d&eacute;fit relev&eacute; haut la main par
Mark Dacascos est d'avoir r&eacute;alis&eacute; lui m&ecirc;me les nombreuses cascades, dont le
fabuleux <I>Butterfly kick</I> du combat final au sabre.
<P>
<IMG SRC="Films/CryingFreeman/emu.gif" ALIGN=RIGHT>
Originaire du Texas, Julie Condra est mannequin et abonn&eacute;e elle aussi aux
s&eacute;ries t&eacute;l&eacute;vis&eacute;es. Elle est apparue dans un petit r&ocirc;le dans
<?php Lien("Films/GasFoodLodging/", "Gas, food, lodging"); ?> de Allison Anders
en 1992, et interpr&egrave;te ici son premier grand r&ocirc;le, celui de la belle et
sensible Emu O'Hara, victime puis amante passionn&eacute;e du Freeman. Christophe Gans
a d&eacute;tect&eacute; en elle un sosie presque parfait du personnage de la bande dessin&eacute;e,
malgr&eacute; ses cheveux courts et blonds !!!
<P>
<IMG SRC="Films/CryingFreeman/netah.gif" ALIGN=LEFT>
De flic v&eacute;reux dans le manga d'origine, Nitta devient Netah, un agent d'Interpol
sp&eacute;cialis&eacute; dans les affaires asiatiques. Interpr&eacute;t&eacute; par Tcheky Karyo, inoubliable
<I>oncle Bob</I> dans <?php Lien("Films/Nikita/", "Nikita"); ?>, Netah est
dandy et bourru, d&eacute;chir&eacute; entre son devoir de policier et la corruption des
yakuza, symbolis&eacute;s d'un c&ocirc;t&eacute; par sa co&eacute;quipi&egrave;re l'inspecteur Forge (personnage
imagin&eacute; par Christophe Gans pour resoudre quelques invraisemblances du manga),
et de l'autre par la fascinante Lady Hanada, dans les bras de laquelle il
perdra d&eacute;finitivement son &acirc;me.
<P>
<IMG SRC="Films/CryingFreeman/kimie.gif" ALIGN=RIGHT>
Cette Lady Hanada, interpr&eacute;t&eacute;e par la belle Yoko Shimada, &eacute;pouse du cruel
Ryuji Hanada, est l'un des personnages les plus int&eacute;ressants du manga.
Encore plus avide de pouvoir que son &eacute;poux, Kimie Hanada &eacute;tait vue par
Christophe Gans comme une mante religieuse. Il explique :
<I>Je savais qu'une asiatique vivant aux Etats Unis ne saurait pas se mouvoir, parler, agir
comme une vraie femme japonaise. La beaut&eacute; et la gr&acirc;ce naturelle de Yoko Shimada ont
apport&eacute; au personnage une authenticit&eacute; que j'aurais moi-m&ecirc;me &eacute;t&eacute; incapable de cr&eacute;er
de toutes pi&egrave;ces</I>.

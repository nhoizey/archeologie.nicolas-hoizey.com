<P>
Le cin&eacute;ma, Mathieu Kassovitz l'a <I>appris en allant dans les salles, pas en lisant les
interviews dans les journaux</I>. Pour lui, <I>ce qui serait merveilleux, c'est que le public
ne sache rien ni du film, ni de celui qui l'a fait, ni des gens qui jouent dedans</I>. Il cite
Stanley Kubrick comme exemple de r&eacute;alisateur qui ne fait aucune concession aux journalistes et
pr&eacute;f&egrave;re que ce soit son film qui soit jug&eacute;, et non son &eacute;quipe.
<P>
A propos de ses performances d'acteur, il explique quelle est sa m&eacute;thode : <I>si tu lis dans le
sc&eacute;nario : le personnage s'assoit sur une chaise, regarde une fourmi et voit l'univers, c'est
beau. A jouer, c'est moins beau. Tu t'assois, il n'y a pas de fourmi, la cam&eacute;ra est sur toi,
et tu dois voir l'univers. Tu fais quoi ? Alors, je propose et je change</I>.
<P>
